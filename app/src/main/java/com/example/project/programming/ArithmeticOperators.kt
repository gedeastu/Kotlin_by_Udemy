package com.example.project.programming

fun main(args: Array<String>) {
    //Operator => +, -, *, /, %

    val number01 : Double = 98.0;
    val number02 : Double = 9.0;

    // +
    val resultIn = number01 + number02
    println(resultIn)

    // -
    val resultDec = number01 - number02
    println(resultDec)

    // *
    val resultMult = number01 * number02
    println(resultMult)

    // /
    val resultDiv = number01 / number02
    println(resultDiv)

    // %
    val resultRemain = number01 % number02
    println(resultRemain)
}