package com.example.byUdemy.programming

fun main(args: Array<String>) {

    var x : Int = 98;
    var y : Int = 9;

    // +=  x = x + y
    x += y
    println(x)

    // -= x = x - y
    x -= y
    println(x)

    // *= x = x * y
    x *= y
    println(x)

    // /= x = x / y
    x /= y
    println(x)

    // %= x = x % y
    x %= y
    println(x)
}