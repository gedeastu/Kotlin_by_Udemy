package com.example.byUdemy.programming

fun main(args: Array<String>) {
    //Byte, Short, Log, Int, Float, Double, Boolean, Char

    //Int
    var age : Int = 19
    println(age)

    //Boolean
    var human : Boolean = true
    println(human)

    //Char
    var operator : Char = '&'
    println(operator)

    //Byte
    var bite : Byte = 16;
    println(bite)

    //Short
    var short : Short = -356;
    println(short)

    //Long
    var long : Long = -432543234L
    println(long)

    //Float
    var float : Float = 5.66F
    println(float)

    //Double
    var double : Double = 3.444
    println(double)

    //String
    var name : String = "Gede Astu Nugraha"
    println(name)
}